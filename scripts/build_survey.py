# 표기 차이 통일 (설문 표기 → 대표 표기)
ALIASES = {
    "코하루라멘": "코하루",
    "맛나는 돈까스": "맛나는 돈가스",
}

CAP_MAP = {"3인": 3, "4인": 4, "이상도 가능!": 99}


def normalize(name: str) -> str:
    """식당명 매칭 키: 공백·괄호 제거, 지점명 접미사 제거"""
    s = re.sub(r"\(.*?\)", "", name)
    s = re.sub(r"\s+", "", s)
    s = re.sub(r"(부산대본점|부산대점|본점|전문점)$", "", s)
    return s


def main():
    agg: dict[str, dict] = {}
    for names, ok, cap in RAW_ROWS:
        for raw in [n.strip() for n in names.split(",")]:
            raw = ALIASES.get(raw, raw)
            key = normalize(raw)
            e = agg.setdefault(key, {"표시명": raw, "언급수": 0, "예": 0, "수용인원": 0})
            e["언급수"] += 1
            e["예"] += 1 if ok == "예" else 0
            e["수용인원"] = max(e["수용인원"], CAP_MAP.get(cap, 0))

    out = {
        k: {
            "표시명": v["표시명"],
            "언급수": v["언급수"],
            "예비율": round(v["예"] / v["언급수"], 4),
            "수용인원": v["수용인원"],
        }
        for k, v in agg.items()
    }
    path = os.path.join(DATA_DIR, "survey.json")
    with open(path, "w", encoding="utf-8") as f:
        json.dump(out, f, ensure_ascii=False, indent=2)
    print(f"saved {len(out)} restaurants -> {path}")


if __name__ == "__main__":
    main()
